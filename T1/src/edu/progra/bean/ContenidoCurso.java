package edu.progra.bean;

public class ContenidoCurso {

	private Integer idContenidoCurso;
	private String nombreTema;
	private String semana;
	private String unidad;
	private String link;
	private Integer idAdministrador;
	public Integer getIdContenidoCurso() {
		return idContenidoCurso;
	}
	public void setIdContenidoCurso(Integer idContenidoCurso) {
		this.idContenidoCurso = idContenidoCurso;
	}
	public String getNombreTema() {
		return nombreTema;
	}
	public void setNombreTema(String nombreTema) {
		this.nombreTema = nombreTema;
	}
	public String getSemana() {
		return semana;
	}
	public void setSemana(String semana) {
		this.semana = semana;
	}
	public String getUnidad() {
		return unidad;
	}
	public void setUnidad(String unidad) {
		this.unidad = unidad;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public Integer getIdAdministrador() {
		return idAdministrador;
	}
	public void setIdAdministrador(Integer idAdministrador) {
		this.idAdministrador = idAdministrador;
	}
	
	
	
	
	
	
	
	
}
